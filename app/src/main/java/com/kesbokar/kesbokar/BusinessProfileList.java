package com.kesbokar.kesbokar;

public class BusinessProfileList {
    private String txtSno;
    private String txtTitle;
    private String txtAbn;
    private String txtPhone;
    private int txtStatus;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTxtSno() {
        return txtSno;
    }

    public void setTxtSno(String txtSno) {
        this.txtSno = txtSno;
    }

    public String getTxtTitle() {
        return txtTitle;
    }

    public void setTxtTitle(String txtTitle) {
        this.txtTitle = txtTitle;
    }

    public String getTxtAbn() {
        return txtAbn;
    }

    public void setTxtAbn(String txtAbn) {
        this.txtAbn = txtAbn;
    }

    public String getTxtPhone() {
        return txtPhone;
    }

    public void setTxtPhone(String txtPhone) {
        this.txtPhone = txtPhone;
    }

    public int getTxtStatus() {
        return txtStatus;
    }

    public void setTxtStatus(int txtStatus) {
        this.txtStatus = txtStatus;
    }
}
