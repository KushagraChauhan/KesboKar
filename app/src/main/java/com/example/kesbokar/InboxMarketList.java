package com.example.kesbokar;

public class InboxMarketList {
    private String txtSno;
    private String txtName;
    private String txtMessage;
    private int txtDate;
    private String txtProduct;

    public int getTxtDate() {
        return txtDate;
    }

    public void setTxtDate(int txtDate) {
        this.txtDate = txtDate;
    }

    public String getTxtProduct() {
        return txtProduct;
    }

    public void setTxtProduct(String txtProduct) {
        this.txtProduct = txtProduct;
    }

    public String getTxtSno() {
        return txtSno;
    }

    public void setTxtSno(String txtSno) {
        this.txtSno = txtSno;
    }

    public String getTxtName() {
        return txtName;
    }

    public void setTxtName(String txtName) {
        this.txtName = txtName;
    }

    public String getTxtMessage() {
        return txtMessage;
    }

    public void setTxtMessage(String txtMessage) {
        this.txtMessage = txtMessage;
    }



}
